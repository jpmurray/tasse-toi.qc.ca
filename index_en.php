          <div class="texteEtape">
            <p> It is totally <strong>unnecessary to board</strong> a car <strong>before the current occupants have disembarked!</strong> This only prevents people from easily getting off the car, and prevents the car from moving on quickly. In the same logic as in Step 1, if people come off the car faster, you can get on more quickly. </p>
          </div>
        </div>
        <!-- End cs_article -->
