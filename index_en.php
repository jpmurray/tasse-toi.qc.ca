            <p> <strong>The small yellow arrows</strong> you can see on the platforms of some stations <strong>are not targets.</strong> They don't indicate where you can stand to get on the car more quickly. Quite the contrary! The yellow lines indicate the boundary zone to keep clear in order to <strong>allow the easy departure of passengers already in the car</strong> getting off at your station.</p>
            <p>Keeping a low-congestion area allows people to empty off the car more quickly, allowing you to get on board just as quickly and smoothly! </p>
          </div>
        </div>
        <!-- End cs_article -->
