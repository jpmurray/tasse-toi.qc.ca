          <div class="texteEtape">
           <p>Once <strong>inside the car,</strong> be logical! If there's space farther down the car, <strong>don't stand just inside the door!</strong> By doing this, you block the entrance and prevent the flow of traffic!</p>
        	<p>The sooner people have gotten off and on the car, the sooner it will go ...and the quality of service will improve! </p>
          </div>
